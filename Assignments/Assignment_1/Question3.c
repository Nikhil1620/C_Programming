#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display 
//  Description :   It is use to Display the numbers in the backward sequence
//  Input :         integer
//  Output :        integer
//  Authur :        Nikhil Ramesh Ahire
//  Date :          19/10/2025
