void Display()
{
    int iCnt = 0;

    for(iCnt = 5; iCnt >=1 ; iCnt = iCnt - 1)
    {
        printf("%d\t", iCnt);
    }
}

/////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function for the Application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}