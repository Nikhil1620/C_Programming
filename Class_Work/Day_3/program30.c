#include<stdio.h>

void Display()
{   
    int iCnt = 0;
    iCnt = 1;
    while(iCnt<=5)
    {
        printf("%d\t",iCnt);  
        iCnt++;
    }

    printf("\n");
}

/////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function for the Application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;

}
